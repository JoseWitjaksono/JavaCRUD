    private void tbDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbDataMouseClicked
        // TODO add your handling code here:
        int baris = tbData.getSelectedRow();
        if(baris != -1)
        {
            tfNis.setText(tbData.getValueAt(baris, 0).toString());
            tfNama.setText(tbData.getValueAt(baris, 1).toString());
            SimpleDateFormat date = new SimpleDateFormat("yyyy-MM-dd");
            Date dateFormat = null;
            try
            {
                dateFormat = date.parse(tbData.getValueAt(baris, 2).toString());
            }
            catch (java.text.ParseException ex) {
                Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
            }
            tfTanggal.setDate(dateFormat);
            if("Laki-Laki".equals(tbData.getValueAt(baris, 3).toString()))
            {
                jkLaki.setSelected(true);
            }
            else
            {
                jkPerempuan.setSelected(true);
            }
            tfKelas.setText(tbData.getValueAt(baris, 4).toString());
            tfEmail.setText(tbData.getValueAt(baris, 5).toString());
            tfAlamat.setText(tbData.getValueAt(baris, 6).toString());
        }
    }//GEN-LAST:event_tbDataMouseClicked

