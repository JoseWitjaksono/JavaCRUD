    public void selectData() 
    {
        String kolom[] = {"NIS","NamaSiswa","TanggalLahir","JenisKelamin","Kelas","Email","Alamat"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM t_siswa";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        
        try
        {
            while(rs.next())
            {
                String NIS = rs.getString(1);
                String NamaSiswa = rs.getString(2);
                String Tanggal = rs.getString(3);
                String JenisKelamin = "";
                   if("L".equals(rs.getString(4)))
                   {
                       JenisKelamin = "Laki-Laki";
                   }
                   else
                   {
                       JenisKelamin = "Perempuan";
                   }
                String Kelas = rs.getString(5);
                String Email = rs.getString(6);
                String Alamat = rs.getString(7);
                String data[] = {NIS,NamaSiswa,Tanggal,JenisKelamin,Kelas,Email,Alamat};
                dtm.addRow(data);
            }
        }
        catch(SQLException ex)
        {
            Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
        }
        tbData.setModel(dtm);
    }
